/**
 * Normalize country code from various formats
 */
export const normalizeCountryCode = (country: string): string => {
  if (!country) return ''
  // Already a 2-letter code
  if (/^[A-Za-z]{2}$/.test(country)) return country.toUpperCase()
  
  // Look up in map
  const normalized = country.toLowerCase().trim()
  return countryMap[normalized] || ''
}

interface CountryFlagProps {
  countryCode: string
  size?: number
  className?: string
}

/**
 * Country Flag Component
 * Displays a country flag based on country code or name
 */
export const CountryFlag: React.FC<CountryFlagProps> = ({ 
  countryCode, 
  size = 12,
  className 
}) => {
  const code = normalizeCountryCode(countryCode)
  if (!code) return (
    <span className={className} style={{ display: 'inline-flex', lineHeight: 0 }}>
      <Globe size={size} style={{ opacity: 0.5 }} />
    </span>
  )
  
  const FlagComponent = flagComponents[code]
  if (!FlagComponent) return (
    <span className={className} style={{ display: 'inline-flex', lineHeight: 0 }}>
      <Globe size={size} style={{ opacity: 0.5 }} />
    </span>
  )
  
  return (
    <span className={className} style={{ display: 'inline-flex', lineHeight: 0 }}>
      <FlagComponent 
        style={{ width: size * 1.5, height: size, borderRadius: 2, display: 'block' }} 
      />
    </span>
  )
}

